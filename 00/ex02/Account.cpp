void	Account::_displayTimestamp(void)
{
	time_t		now = time(0);
    struct tm	tm_now = *localtime(&now);
	
	std::cout << "[";
	std::cout << std::setfill('0');
	std::cout << std::setw(4) << 1900 + tm_now.tm_year;
	std::cout << std::setw(2) << 1 + tm_now.tm_mon;
	std::cout << std::setw(2) << tm_now.tm_mday;
	std::cout << "_";
	std::cout << std::setw(2) << tm_now.tm_hour;
	std::cout << std::setw(2) << tm_now.tm_min;
	std::cout << std::setw(2) << tm_now.tm_sec;
	std::cout << "] ";

	// std::cout << "[19920104_091532] ";
}
